        <p class="explain">また、漁業でつかっている網（定置網など）に引っ掛けて壊してしまった場合も、規模によっては数億円の請求が来る場合があります。それは網の修理代金に加えて、<strong>網が壊れたことによって損失した水揚げ額があるから</strong>です。</p>
        <p class="explain">そういった賠償金をカバーするために<strong>保険金額は最低でも３億円の加入が好ましい</strong>と言えるでしょう。さらに安心されたい方は責任制限法との兼ね合いで６億円に加入すれば全く問題がないと言えるでしょう。</p><p class="explain">６億円と聞けばそんなにいらないと思うかもしれませんが、自動車保険でいう「<strong>無制限</strong>」みたいなものを思っておきましょう。</p>    
        <h3 class="h3_explain">PB責任保険ワイドは責任保険をカバー</h3>
        <p class="explain">次にPB責任保険ワイドですが、これはPB責任保険で対象にならなかった部分を補う事が出来る保険です。<br>基本的には<strong>対人対物の保険</strong>ですが、例えばPB責任保険では対象にならなかった<strong>水面撤去費用等</strong>が対象になります。</p>
        <p class="explain">また、ＰＢ搭乗者傷害保険やＰＢ乗客賠償責任保険に加入する場合には<strong>必ずつけなければいけない保険（付帯保険と言います。）</strong>です。５トン未満で４，０００円、５トン以上で８，０００円の保険料なので、さほど高額ではなく加入しやすい保険と言えるでしょう。</p>
        
        <h3 class="h3_explain">PB搭乗者傷害保険</h3>
        <p class="explain">PB搭乗者傷害保険は運転手（船主）やその友人などがケガや死亡したときにお見舞い金という形で支払われる保険です。<br>加入は完全に任意（加入人数も自由）ですが、いつも乗るメンバーの数に５００万～１，５００万円くらいの保険金額をかけておけば安心でしょう。</p>
        <h3 class="h3_explain">ＰＢ乗客賠償責任保険</h3>
        <p class="explain">遊漁船や旅客船で商売をする船が必ず加入しなければいけないのが、この<strong>ＰＢ乗客賠償責任保険</strong>です。<br>遊漁の許可を取る関係で船舶検査証書の旅客人数で、さらには一人３，０００万円以上での加入が必須です。</p>
