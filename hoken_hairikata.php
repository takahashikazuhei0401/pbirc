        <h3 class="h3_explain">PB責任保険（対人対物）は必須の保険</h3>
        <p class="explain">まず、必ず入らなくてはいけないのが<strong>PB責任保険</strong>です。PB責任保険は「<strong>対人・対物</strong>」の保険です。つまり相手の船や遊泳者に自分の船をぶつけてしまった際、相手から賠償が来た場合に保険金が出ます。</p>
        <p class="explain">人なんてひかないから大丈夫！と思っている人もいるでしょうけど、車の運転と一緒で何が起こるかわかりません。</p>
